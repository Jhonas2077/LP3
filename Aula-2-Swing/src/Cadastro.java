    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       if(rdo_Masculino.isSelected()){
           sexo = 'M';
           JOptionPane.showMessageDialog(null," Sexo selecionado:  " + sexo, "Alerta!", JOptionPane.OK_OPTION);
           
       }
       else  if(rdo_Feminino.isSelected()){
           sexo = 'F';
           JOptionPane.showMessageDialog(null," Sexo selecionado:  " + sexo, "Alerta!", JOptionPane.OK_OPTION);
    }//GEN-LAST:event_btnSalvarActionPerformed

       else {
           JOptionPane.showMessageDialog(null, "Selecione um sexo  ", "Erro", JOptionPane.ERROR_MESSAGE);
       }
      
      if(chk_Design.isSelected()){ 
       design = true;   
      }
      else{
       design = false;
      } 
      
       if(chk_Musicas.isSelected()){ 
       musicas = true;   
      }
      else{
       musicas = false;
      } 
       if(chk_Tecnologias.isSelected()){ 
       tecnologias = true;   
      }
      else{
       tecnologias = false;
      } 
      
      JOptionPane.showMessageDialog(null, "Interesses em:\nMusicas "+musicas +"\nDesign: "+design +"\nTecnologias: "+tecnologias,
              "Alerta", 1); 
      
      JOptionPane.showMessageDialog(null,"O idioma selecionado Ã©: "+cmb_Idioma.getSelectedItem(),"Alerta",2);
      
      Pessoa p = new Pessoa(txtNome.getText(),sexo,(String)cmb_Idioma.getSelectedItem()); 
      
      DefaultTableModel tbl = (DefaultTableModel) tabela.getModel();
      tbl.addRow(p.obterDados());
      
      txtNome.setText( "");
      buttonGroup1.clearSelection();
      chk_Design.setSelected( false);
      chk_Musicas.setSelected( false);
      chk_Tecnologias.setSelected( false);
      cmb_Idioma.setSelectedIndex(0);
      
    }
  
    
